package section02;
/*
 * 변수(Variable)
 * 데이터를 저장하고 참조하기 위한 메모리 공간을 나타내는 이름
 * 변수는 이름을 통해 해당 메모리 공간에 접근하고 값을 읽거나 수장할 수 있다
 * 
 * 변수 선언 및 초기화
 *   자료형 변수명 = 값;
 * 
 * 
 * 
 */
public class variable {
	public static void main(String[] args) {
		// 정수형 변수 선언하고 값 대입하기
		int num1 = 10;
		
		//실수형 변수 선언하고 값 대입하기
		double num2 = 3.14;
		
		//문자열 변수 선언하고 값 대입히가
		String str = "Hello";
		
		
	}
	

}
